

# Solicitar al usuario el lado del cubo
lado = float(input("Introduce el lado del cubo: "))

# Calcular el área del cubo
area = 6 * (lado ** 2)

# Calcular el volumen del cubo
volumen = lado ** 3

# Mostrar los resultados
print(f"El área del cubo es: {area}")
print(f"El volumen del cubo es: {volumen}")